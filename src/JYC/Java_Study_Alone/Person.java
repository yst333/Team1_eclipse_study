package JYC.Java_Study_Alone;
//Practice_Stream_Scanner_Arraylist의 실행클래스의 참조가 되는 객체클래스입니다.
public class Person {
	    private String name;
	    private int age;

	    
	    public Person(String name, int age) {
	    	this.name = name;
	    	this.age = age;
	    }
	    


		public String getName() {
	        return name;
	    }


		public int getAge() {
	        return age;
	    }
	}


